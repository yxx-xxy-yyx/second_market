-- 1. 创建更新时间自动更新函数
BEGIN;

CREATE OR REPLACE FUNCTION set_updated_time()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_time = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION set_update_time()
RETURNS TRIGGER AS $$
BEGIN
  NEW.update_time = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- 2. 创建学校表
CREATE TABLE IF NOT EXISTS school (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  school_code VARCHAR(50) NOT NULL UNIQUE,
  name_zh VARCHAR(255) NOT NULL,
  name_ko VARCHAR(255) NOT NULL,
  name_en VARCHAR(255) NOT NULL,
  city_zh VARCHAR(100),
  city_ko VARCHAR(100),
  city_en VARCHAR(100),
  logo_url VARCHAR(500),
  status SMALLINT DEFAULT 1,
  deleted INTEGER DEFAULT 0,
  created_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 创建学校表更新时间触发器
DROP TRIGGER IF EXISTS trg_school_updated_time ON school;
CREATE TRIGGER trg_school_updated_time
BEFORE UPDATE ON school
FOR EACH ROW EXECUTE FUNCTION set_updated_time();

-- 3. 创建用户表
CREATE TABLE IF NOT EXISTS sys_user (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username VARCHAR(50) NOT NULL,
  nickname VARCHAR(50) NOT NULL,
  password VARCHAR(100) NOT NULL,
  email VARCHAR(100),
  phone VARCHAR(20),
  address VARCHAR(255),
  gender CHAR(1) DEFAULT 'U',
  age INTEGER,
  bio VARCHAR(500),
  avatar VARCHAR(255),
  role VARCHAR(20) NOT NULL DEFAULT 'user',
  status INTEGER NOT NULL DEFAULT 1,
  credit_score INTEGER NOT NULL DEFAULT 100,
  school_id BIGINT,
  create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted INTEGER NOT NULL DEFAULT 0,
  CONSTRAINT uk_username UNIQUE (username),
  CONSTRAINT uk_email UNIQUE (email),
  CONSTRAINT fk_user_school FOREIGN KEY (school_id) REFERENCES school (id) ON DELETE SET NULL ON UPDATE CASCADE
);

-- 创建用户表更新时间触发器
DROP TRIGGER IF EXISTS trg_sys_user_update_time ON sys_user;
CREATE TRIGGER trg_sys_user_update_time
BEFORE UPDATE ON sys_user
FOR EACH ROW EXECUTE FUNCTION set_update_time();

