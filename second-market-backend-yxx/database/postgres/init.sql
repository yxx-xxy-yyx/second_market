CREATE TABLE school (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  school_code VARCHAR(50) NOT NULL UNIQUE,
  name_zh VARCHAR(255) NOT NULL,
  name_ko VARCHAR(255) NOT NULL,
  name_en VARCHAR(255) NOT NULL,
  city_zh VARCHAR(100),
  city_ko VARCHAR(100),
  city_en VARCHAR(100),
  logo_url VARCHAR(500),
  status SMALLINT DEFAULT 1,
  deleted INTEGER DEFAULT 0,
  created_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

DROP TRIGGER IF EXISTS trg_school_updated_time ON school;
CREATE TRIGGER trg_school_updated_time
BEFORE UPDATE ON school
FOR EACH ROW EXECUTE FUNCTION set_updated_time();

